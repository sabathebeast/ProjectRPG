void Map::loadMap(int arr[64][64])
{
	for (int row = 0; row < 64; row++)
	{
		for (int column = 0; column < 64; column++)
		{
			map[row][column] = arr[row][column];
		}
	}
}

void Map::drawMap(Texture tex0, Texture tex1, Texture tex2)
{
	for (int row = 0; row < 64; row++)
	{
		for (int column = 0; column < 64; column++)
		{
			if (map[row][column] == 0)
			{
				DrawTexture(tex0, column * tileSize, row * tileSize, WHITE);
			}
			else if (map[row][column] == 1)
			{
				DrawTexture(tex1, column * tileSize, row * tileSize, WHITE);
			}
			else if (map[row][column] == 2)
			{
				DrawTexture(tex2, column * tileSize, row * tileSize, WHITE);
			}
		}
	}
}
